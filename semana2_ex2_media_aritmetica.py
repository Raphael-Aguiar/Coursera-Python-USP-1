# Faça um programa em Python que receba quatro notas, calcule e imprima a média aritmética.
primeira_nota = input ("Digite a primeira nota: ")
segunda_nota = input ("Digite a segunda nota: ")
terceira_nota = input ("Digite a terceira nota: ")
quarta_nota = input ("Digite a quarta nota: ")
soma_final = int (primeira_nota) + int (segunda_nota) + int (terceira_nota) + int (quarta_nota)
media_final= soma_final/4
print("A média aritmética final é", media_final)